import matplotlib.pyplot as plt

# Приклад даних
week_days = ['Понеділок', 'Вівторок', 'Середа', 'Четвер', 'Пятниця', 'Субота', 'Неділя']
views = [0, 10, 42, 125, 379, 581, 1989]

# Створення лінійного графіка
plt.plot(week_days, views)

# Додавання підписів
plt.title("Як росте кількість переглядів відео з котиками")
plt.xlabel("День тижня")
plt.ylabel("Кількість переглядів")

# Показати графік
plt.show()

# plt.plot() — будує лінію на графіку, яка відображає кількість переглядів відео з котиками.
# plt.title(), plt.xlabel(), plt.ylabel() — додають заголовок та підписи до осей.
# plt.show() — відображає графік.